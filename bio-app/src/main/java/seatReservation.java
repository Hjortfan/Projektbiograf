
public class seatReservation {
	
	private int reservation_id, seat_id, screenings_id;
	
	seatReservation( int reservaton_id, int seat_id, int screenings_id ){
		this.reservation_id=reservaton_id;
		this.seat_id=seat_id;
		this.screenings_id=screenings_id;
	}
	
	public int getReservationsId() {
		return reservation_id;
	}
	
	public int getSeatId() {
		return reservation_id;
	}
	
	public int getScreeningId() {
		return reservation_id;
	}

}
